#!/usr/bin/env node
/**
 * Token manifest generator (v37 T3).
 *
 * Parses the cascivo token + theme CSS and emits, from one source of truth:
 *   1. packages/tokens/tokens.json   — { name, category, role, value, canonical, aliasOf? }[]
 *   2. packages/tokens/src/tokens.d.ts — `CascivoToken` / `CascivoColorToken` unions (autocomplete)
 *   3. docs/TOKENS.md                 — a human reference table, canonical names flagged
 *
 * This ends the "grep node_modules to find the tokens" experience (#8) and makes
 * the canonical-vs-alias choice explicit (#7) without removing any token.
 *
 * Run via `pnpm regen` (and `pnpm tokens:generate`); drift-checked in CI.
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const REPO_ROOT = join(import.meta.dirname, '..', '..')
const TOKENS_CSS = join(REPO_ROOT, 'packages/tokens/src/index.css')
const LIGHT_CSS = join(REPO_ROOT, 'packages/tokens/../themes/src/light.css')

// Canonical/alias map (#7). Key = alias token, value = its canonical token.
// Canonical names follow the choices the boringtools report adopted:
// foreground, text-muted, destructive.
const ALIASES = {
  '--cascivo-color-bg': '--cascivo-color-background',
  '--cascivo-color-text': '--cascivo-color-foreground',
  '--cascivo-color-foreground-muted': '--cascivo-color-text-muted',
  '--cascivo-color-error': '--cascivo-color-destructive',
  '--cascivo-color-accent-content': '--cascivo-color-accent-foreground',
  '--cascivo-color-success-content': '--cascivo-color-success-foreground',
  '--cascivo-color-warning-content': '--cascivo-color-warning-foreground',
  '--cascivo-color-destructive-content': '--cascivo-color-destructive-foreground',
  '--cascivo-color-primary-content': '--cascivo-color-primary-fg',
}

/** @param {string} name */
function categorize(name) {
  const n = name.replace('--cascivo-', '')
  if (
    n.startsWith('color-') ||
    n.startsWith('gray-') ||
    n.startsWith('blue-') ||
    n.startsWith('green-') ||
    n.startsWith('red-') ||
    n.startsWith('orange-') ||
    n.startsWith('yellow-') ||
    n.startsWith('warm-') ||
    n.startsWith('chart-') ||
    n.startsWith('brand-') ||
    n.startsWith('border-') ||
    n.startsWith('ring-') ||
    n.startsWith('focus')
  )
    return 'color'
  if (n.startsWith('space-')) return 'space'
  if (n.startsWith('radius-')) return 'radius'
  if (n.startsWith('shadow-')) return 'shadow'
  if (
    n.startsWith('text-') ||
    n.startsWith('font-') ||
    n.startsWith('leading-') ||
    n.startsWith('tracking-')
  )
    return 'typography'
  if (n.startsWith('duration-') || n.startsWith('ease-') || n.startsWith('motion-')) return 'motion'
  if (n.startsWith('z-')) return 'z-index'
  if (n.startsWith('screen-')) return 'breakpoint'
  if (n.startsWith('control-height-') || n.startsWith('target-min')) return 'sizing'
  if (n.startsWith('shell-')) return 'layout'
  return 'other'
}

/**
 * Extract `--cascivo-name: value;` declarations. First declaration per name wins
 * (so the :root value beats a later @media prefers-reduced-motion override).
 * @param {string} css
 * @param {Map<string,string>} into
 */
function extractTokens(css, into) {
  const re = /(--cascivo-[\w-]+)\s*:\s*([^;]+);/g
  let m
  while ((m = re.exec(css)) !== null) {
    const name = m[1]
    const value = m[2].replace(/\s+/g, ' ').trim()
    if (!into.has(name)) into.set(name, value)
  }
}

// index.css = primitives + semantic defaults; light.css = representative theme
// values for semantic tokens. Light wins for tokens it redefines.
const values = new Map()
extractTokens(readFileSync(LIGHT_CSS, 'utf8'), values)
extractTokens(readFileSync(TOKENS_CSS, 'utf8'), values)

const tokens = [...values.entries()]
  .map(([name, value]) => {
    const aliasOf = ALIASES[name]
    /** @type {{name:string,category:string,role:string,value:string,canonical:boolean,aliasOf?:string}} */
    const entry = {
      name,
      category: categorize(name),
      role: name.replace('--cascivo-', ''),
      value,
      canonical: !aliasOf,
    }
    if (aliasOf) entry.aliasOf = aliasOf
    return entry
  })
  .sort((a, b) => a.category.localeCompare(b.category) || a.name.localeCompare(b.name))

// 1. tokens.json
writeFileSync(
  join(REPO_ROOT, 'packages/tokens/tokens.json'),
  `${JSON.stringify(tokens, null, 2)}\n`,
)

// 2. tokens.d.ts
const allNames = tokens.map((t) => t.name).sort()
const colorNames = tokens
  .filter((t) => t.category === 'color')
  .map((t) => t.name)
  .sort()
const union = (names) => names.map((n) => `  | '${n}'`).join('\n')
const dts = `// AUTO-GENERATED by scripts/tokens/generate-manifest.mjs — do not edit by hand.
// Run \`pnpm tokens:generate\` (or \`pnpm regen\`) to refresh.

/** Every \`--cascivo-*\` custom property cascivo ships (primitive + semantic). */
export type CascivoToken =
${union(allNames)}

/** The subset of {@link CascivoToken} in the color category. */
export type CascivoColorToken =
${union(colorNames)}
`
writeFileSync(join(REPO_ROOT, 'packages/tokens/src/tokens.d.ts'), dts)

// 3. docs/TOKENS.md
const byCategory = new Map()
for (const t of tokens) {
  if (!byCategory.has(t.category)) byCategory.set(t.category, [])
  byCategory.get(t.category).push(t)
}
const CATEGORY_ORDER = [
  'color',
  'typography',
  'space',
  'radius',
  'shadow',
  'motion',
  'sizing',
  'layout',
  'z-index',
  'breakpoint',
  'other',
]
const sortedCategories = [...byCategory.keys()].sort(
  (a, b) => CATEGORY_ORDER.indexOf(a) - CATEGORY_ORDER.indexOf(b),
)
let md = `# cascivo Design Tokens

> **Generated** from \`packages/tokens/src/index.css\` + \`packages/themes/src/light.css\`
> by \`scripts/tokens/generate-manifest.mjs\`. Do not edit by hand — run
> \`pnpm tokens:generate\` (or \`pnpm regen\`).

Every value cascivo exposes as a \`--cascivo-*\` CSS custom property. Values shown
are the **light** theme's; theme-scoped tokens (colors, shadows) differ per
\`[data-theme]\`. A machine-readable manifest is published at
\`@cascivo/tokens/tokens.json\`, and a \`CascivoToken\` union for editor autocomplete
at \`@cascivo/tokens/tokens\`.

## Canonical names vs aliases

Some roles have more than one token name for historical reasons (#7). The
**canonical** name is the one to prefer; aliases resolve to the same value and
are kept for backwards-compatibility (no token has been removed).

| Role | Canonical | Alias |
| ---- | --------- | ----- |
${Object.entries(ALIASES)
  .map(
    ([alias, canonical]) =>
      `| ${canonical.replace('--cascivo-color-', '')} | \`${canonical}\` | \`${alias}\` |`,
  )
  .join('\n')}
`

for (const category of sortedCategories) {
  const list = byCategory.get(category)
  md += `\n## ${category}\n\n| Token | Value | Notes |\n| ----- | ----- | ----- |\n`
  for (const t of list) {
    const name = t.canonical ? `**\`${t.name}\`**` : `\`${t.name}\``
    const notes = t.canonical ? '' : `alias of \`${t.aliasOf}\``
    md += `| ${name} | \`${t.value}\` | ${notes} |\n`
  }
}
writeFileSync(join(REPO_ROOT, 'docs/TOKENS.md'), md)

console.log(
  `✓ tokens: ${tokens.length} tokens → tokens.json + tokens.d.ts + docs/TOKENS.md ` +
    `(${tokens.filter((t) => !t.canonical).length} aliases)`,
)
